/* C++ program to find Square Root of a Number */
#include<iostream>
#include<math.h>
using namespace std;
 
int main(){
    cout<<"-------Find Square Root of a Number-------\n\n";
    float num;
    cout<<"Enter a Number : ";
    cin>>num;

    double sq;
    sq=sqrt(num);

    cout<<"\nThe square root of the number is : "<<sq;



return 0;
}