/* C++ program to find Square Root of a Number */

#include<iostream>
#include<cmath>
using namespace std;
 
int main(){
    cout<<"-----Find Square Root of a Number-------\n\n";

    int num;
    cout<<"Enter a Number : ";
    cin>>num;

    float s=sqrt(num);

    cout<<"\nThe Square root of "<<num<<" is : "<<s;





return 0;
}